package org.example.hw2;

import java.util.Scanner;

public class Task2 {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);

        // Читаем количество чисел
        int n = input.nextInt();

        // Читаем первое число и предполагаем, что последовательность возрастает
        int previous = input.nextInt();
        boolean increasing = true;

        // Обходим каждое число и проверяем, возрастает ли последовательность
        for (int i = 1; i < n; i++) {
            int current = input.nextInt();
            if (current <= previous) {
                increasing = false;
                break;
            }
            previous = current;
        }

        if (increasing) {
            System.out.println("Последовательность возрастающая");
        } else {
            System.out.println("Последовательность не является возрастающей");
        }
    }
}

// Этот код читает количество чисел n и последовательность целых чисел из стандартного ввода.
// Мы начинаем считать последовательность возрастающей, предполагая, что первое число больше или равно нулю.
// Затем мы обходим каждое число и сравниваем его со значением предыдущего числа. Если текущее число меньше или равно предыдущему, то последовательность не является возрастающей, и мы выходим из цикла.
// Если мы дошли до конца последовательности и не нашли нарушений возрастания, то последовательность является возрастающей.
// В зависимости от результата проверки, мы выводим соответствующее сообщение в стандартный вывод.